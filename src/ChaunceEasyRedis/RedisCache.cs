        /// <typeparam name="T">The 1st type parameter.</typeparam>
        public async Task<T> GetAsync<T>(string key, Func<Task<T>> factory = null, TimeSpan? expiration = default(TimeSpan?)) where T : class
        {
            ArgumentCheck.NotNullOrWhiteSpace(key, nameof(key));
            string oldKey = key;
            key = AddSysCustomKey(key);

            var _db = _dbProvider.GetDatabase();

            var result = await _db.StringGetAsync(key);
            if (!result.IsNull)
            {
                oldKey = null;
                return ConvertObj<T>(result);
            }
            else
            {
                var item = await factory?.Invoke();
                if (item != null)
                {
                    await SetAsync(oldKey, item, expiration);
                    return item;
                }
                return default(T);
            }
        }

