        /// </summary>
        /// <param name="key">Redis Key</param>
        /// <param name="value">保存的值</param>
        /// <param name="expiry">过期时间</param>
        /// <returns></returns>
        public bool Set(string key, object obj, TimeSpan? expiry = default(TimeSpan?))
        {
            if (obj == null)
            {
                throw new Exception("Can not insert null values to the cache!");
            }
            var entryBytes = Serialize(obj);
            key = AddSysCustomKey(key);
            return Do(db => db.StringSet(key, entryBytes, expiry));
        }

        /// <summary>
        /// 保存多个key value
