        /// <summary>
        /// 获取指定key的值
        /// </summary>
        /// <typeparam name="T"></typeparam>
        /// <param name="key"></param>
        /// <returns></returns>
        public T Get<T>(string key)
        {
            ArgumentCheck.NotNullOrWhiteSpace(key, nameof(key));

            key = AddSysCustomKey(key);
            var _db = _dbProvider.GetDatabase();
            var rValue = _db.StringGet(key);
            if (!rValue.HasValue)
                return default(T);
            var result = Deserialize<T>(rValue);
            return result;
        }

        /// <summary>
        /// 获取指定Key的值，没有则取出委托函数的值并且放回redis中
        /// </summary>
        /// <returns>The get.</returns>
        /// <param name="cacheKey">Cache key.</param>
        /// <param name="factory">Data retriever.</param>
        /// <param name="expiration">Expiration.</param>
        /// <typeparam name="T">The 1st type parameter.</typeparam>
        public T Get<T>(string key, Func<T> factory = null, TimeSpan? expiration = default(TimeSpan?)) where T : class
        {
            ArgumentCheck.NotNullOrWhiteSpace(key, nameof(key));
            string oldKey = key;

            key = AddSysCustomKey(key);

            var _db = _dbProvider.GetDatabase();

            var result = _db.StringGet(key);
            if (!result.IsNull)
            {
                oldKey = null;
                //return Deserialize<T>(result);
                return ConvertObj<T>(result);
            }
            else
            {
                var item = factory?.Invoke();
                lock (SyncObj)
                {
                    result = _db.StringGet(key);
                    if (result.HasValue)
                    {
                        return Deserialize<T>(result);
                    }
                    else
                    {
                        if (item == null)
                        {
                            return default(T);
                        }
                        else
                        {
                            Set(oldKey, item, expiration);
                            return item;
                        }
                    }
                }
            }
        }

